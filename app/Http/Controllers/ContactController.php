     */
    public function sendEmail(Request $request)
    {
        $receiverEmailAddress = "contact@tangagroup.com";
       Mail::to($receiverEmailAddress)->send(new ContactMail);
       if (Mail::flushMacros() != 0)
       {
        return "Oops! Votre message n'a pas été envoyé.";
        }

       return "Votre message a bien été envoyé!";
    }

